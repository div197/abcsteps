/**
 * Get a random encouragement in the specified language
 */
export function getRandomEncouragement(languageCode: string): string {
  const langData = guruMultilingualExamples[languageCode as keyof typeof guruMultilingualExamples];
  if (!langData || !langData.encouragements) {
    return guruMultilingualExamples.en.encouragements[0];
  }
  
  const encouragements = langData.encouragements;
  return encouragements[Math.floor(Math.random() * encouragements.length)];
}

/**
 * Get example response for a specific topic and language
 */
export function getExampleResponse(languageCode: string, topic: string): any {
  const langData = guruMultilingualExamples[languageCode as keyof typeof guruMultilingualExamples];
  if (!langData || !langData.examples) {
    return null;
  }
  
  return langData.examples.find(ex => ex.topic.toLowerCase().includes(topic.toLowerCase()));
}